$(document).ready(function(){
    $("#btn-close-1").click(function(){
    $("#modal-container-1").hide();
    $("#one-modal").hide();
    $("body").css("overflow", "auto");
    setTimeout(function() {
      $("#modal-container-1").css({
        transform: "scale(1)",
        opacity: 0,
      });
    }, 300);
 });
$("#btn-open-1").click(function(){
    $("#modal-container-1").show();
    $("#one-modal").show();
    $("body").css("overflow", "hidden");
    setTimeout(function() {
      $("#modal-container-1").css({
        transform: "scale(1.1)", // Phóng to một chút
        opacity: 1,
      });
    }, 100);
});
});



