    $(".decrease").click(function () {
      let quantityInput = $(this).siblings(".quantity-value");
      let currentQuantity = parseInt(quantityInput.val());
      if (currentQuantity > 1) {
        quantityInput.val(currentQuantity - 1);
      }
    });
  
